export const messages = {
  en,
  'zh-CN': zhCN,
} as const;

export type Locale = keyof typeof messages;
export type TranslationKey = keyof typeof en;
type TranslationParams = Record<string, string | number>;
type CountUnit = 'photo' | 'folder' | 'library' | 'account' | 'source' | 'original' | 'media' | 'album';
type StorageLike = Pick<Storage, 'getItem' | 'setItem'>;

const storageKey = 'dk-photo-locale';
const fallbackLocale: Locale = 'en';
const countUnitKeys: Record<CountUnit, { one: TranslationKey; other: TranslationKey }> = {
  photo: { one: 'common.photoUnit', other: 'common.photosUnit' },
  media: { one: 'common.mediaUnit', other: 'common.mediaItemsUnit' },
  album: { one: 'common.albumUnit', other: 'common.albumsUnit' },
  folder: { one: 'common.folderUnit', other: 'common.foldersUnit' },
  library: { one: 'common.libraryUnit', other: 'common.librariesUnit' },
  account: { one: 'common.accountUnit', other: 'common.accountsUnit' },
  source: { one: 'common.sourceUnit', other: 'common.sourcesUnit' },
  original: { one: 'common.originalUnit', other: 'common.originalsUnit' },
};
const locale = ref<Locale>(resolveInitialLocale(readStoredLocale(), readBrowserLanguages()));

applyLocale(locale.value);

export function normalizeLocale(value: string | null | undefined): Locale | null {
  if (!value) return null;
  const normalized = value.toLowerCase();
  if (normalized === 'zh-cn' || normalized === 'zh' || normalized.startsWith('zh-')) return 'zh-CN';
  if (normalized === 'en' || normalized.startsWith('en-')) return 'en';
  return null;
}

export function resolveInitialLocale(stored: string | null | undefined, languages: readonly string[] = []): Locale {
  const storedLocale = normalizeLocale(stored);
  if (storedLocale) return storedLocale;
  return languages.some((language) => normalizeLocale(language) === 'zh-CN') ? 'zh-CN' : fallbackLocale;
}

export function translateWithLocale(targetLocale: Locale, key: TranslationKey, params: TranslationParams = {}) {
  const template = messages[targetLocale][key] || messages.en[key] || `[${key}]`;
  return interpolate(template, params);
}

export function nextLocale(value: Locale): Locale {
  return value === 'zh-CN' ? 'en' : 'zh-CN';
}

export function persistLocale(value: Locale, storage: StorageLike | null = browserStorage()) {
  storage?.setItem(storageKey, value);
}

export function useLocale() {
  const isChinese = computed(() => locale.value === 'zh-CN');
  const languageLabel = computed(() => (isChinese.value ? '中' : 'EN'));

  function t(key: TranslationKey, params: TranslationParams = {}) {
    return translateWithLocale(locale.value, key, params);
  }

  function setLocale(value: Locale) {
    locale.value = value;
    applyLocale(value);
  }

  function toggleLocale() {
    setLocale(nextLocale(locale.value));
  }

  function formatCount(count: number, unit: CountUnit) {
    const value = count.toLocaleString(locale.value);
    return `${value} ${messages[locale.value][unitKey(unit, count)]}`;
  }

  function formatDate(raw: string | number | Date | null | undefined) {
    if (!raw) return t('common.noDate');
    return new Date(raw).toLocaleDateString(locale.value);
  }

  function formatDateTime(raw: string | number | Date | null | undefined, fallback: TranslationKey = 'common.noDate') {
    if (!raw) return t(fallback);
    return new Date(raw).toLocaleString(locale.value);
  }

  return {
    locale,
    isChinese,
    languageLabel,
    setLocale,
    toggleLocale,
    t,
    formatCount,
    formatDate,
    formatDateTime,
  };
}

function unitKey(unit: CountUnit, count: number): TranslationKey {
  return count === 1 ? countUnitKeys[unit].one : countUnitKeys[unit].other;
}

function interpolate(template: string, params: TranslationParams) {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

function applyLocale(value: Locale) {
  documentElement()?.setAttribute('lang', value);
  persistLocale(value);
}

function readStoredLocale() {
  return browserStorage()?.getItem(storageKey);
}

function readBrowserLanguages() {
  if (typeof navigator === 'undefined') return [];
  return navigator.languages?.length ? navigator.languages : [navigator.language].filter(Boolean);
}

function browserStorage(): StorageLike | null {
  if (typeof localStorage === 'undefined') return null;
  return localStorage;
}

function documentElement() {
  if (typeof document === 'undefined') return null;
  return document.documentElement;
}
